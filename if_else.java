public class if_else {
    public static void main(String args[]){
        int a=8;
        int b=8;
        if(a>b){
            System.out.println(a);
        }
        else if(a==b){
            System.out.println("Both are equal");
        }
        else{
            System.out.println(b);
        }
    }
}
